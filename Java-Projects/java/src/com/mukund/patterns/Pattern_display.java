package com.mukund.patterns;

/**
 * The PatternDisplay class displays a rectangular pattern using the "@" character.
 */
public class Pattern_display {

    public static void main(String[] args) {
        // Display the top border of the rectangle
        System.out.println("@@@@@@@@");
        
        // Display the sides of the rectangle with empty space in between
        System.out.println("@      @\n@      @\n@      @\n@      @\n@      @");
        
        // Display the bottom border of the rectangle
        System.out.println("@@@@@@@@");
    }
}
